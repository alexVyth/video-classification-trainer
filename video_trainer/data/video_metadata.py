from dataclasses import dataclass

from video_trainer.enums import Dataset
from video_trainer.settings import FPS, VIDEO_DURATION_IN_SECONDS


@dataclass
class VideoData:
    dataset: Dataset
    name: str
    first_annotated_frame: int
    last_video_frame: int

    @property
    def last_frame(self) -> int:
        return min(
            [
                self.first_annotated_frame + (VIDEO_DURATION_IN_SECONDS * FPS),
                self.last_video_frame - 1,
            ]
        )


